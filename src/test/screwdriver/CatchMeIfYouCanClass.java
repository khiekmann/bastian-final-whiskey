public class CatchMeIfYouCanClass
{
	public static int publicStaticMethodAddsFirstAndSecondParameter(int firstParameter, int secondParameter) {
		return firstParameter + secondParameter;
	}

	private int privateMethodAddsFirstAndSecondParameter(int firstParameter, int secondParameter) {
		return firstParameter + secondParameter;
	}
}
