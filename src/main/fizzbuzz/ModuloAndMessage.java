public class ModuloAndMessage
{
	private int modulo;
	private String message;

	public ModuloAndMessage(int modulo, String message)
	{
		this.modulo = modulo;
		this.message = message;
	}

	public int getModulo()
	{
		return modulo;
	}

	public String getMessage()
	{
		return message;
	}
}
